// tipe data boolean
// dengan nilai true / false
var nilai = 100;
var status;

if (nilai > 99)
status = true;
else 
status = false;

console.log(status); // true

// tipe data null / undefined
var isi = null;
var kosong;
console.log(isi); // null
console.log(kosong); // undefined 